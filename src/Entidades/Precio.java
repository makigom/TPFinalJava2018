package Entidades;
import java.util.Date;

public class Precio {
	
	Date fecha;
	float precio;
	
	public Date getFecha()
	{
		return fecha;
	}
	
	public void setFecha(Date fec)
	{
		fecha = fec;
	}
	
	public float getPrecio()
	{
		return precio;
	}
	
	public void setPrecio(float prec)
	{
		precio = prec;
	}

}
